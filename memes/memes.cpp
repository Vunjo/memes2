#include <cstdio> //stdio == standard input output

int main() {
	int a = 5; //integer - cijeli broj
	long long d = 10; //integer 64bit
	float b = 5.5f; //realni broj
	double c = 5.7; //realni broj 64 bit
	bool e = true; //true ili false (1 ili 0)
	char f = '8'; //character

	printf("%d %lld %f %lf %d %c\n", a, d, b, c, e, f);
	return 0;
}
